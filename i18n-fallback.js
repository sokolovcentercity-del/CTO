/**
 * Подставляет значения плейсхолдеров {key} в строку.
 */
function applyVars(str, vals) {
  if (!vals || typeof str !== 'string') return str;
  return str.replace(/\{(\w+)\}/g, (_, k) => (vals[k] !== undefined ? String(vals[k]) : '{' + k + '}'));
}

/**
 * Устанавливает window.miniappI18n БЕЗУСЛОВНО.
 * Вызывается как можно раньше — до загрузки остальных модулей.
 * Не проверяем window.miniappI18n — перезаписываем всегда,
 * чтобы платформа не подставила английский перевод.
 */
export function installI18nFallback() {
  window.miniappI18n = {
    t(key, vals) {
      const str = RU[key];
      if (str === undefined) return key;
      return applyVars(str, vals);
    },
    getContext() {
      return { resolvedLocale: 'ru', dir: 'ltr', availableLocales: ['ru'], canChangeLocale: false };
    },
  };
}
